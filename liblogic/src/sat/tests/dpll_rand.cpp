#include <gtest/gtest.h>
#include <dpll_rand.h>

TEST(DPLL_RANDTest, SATSmallSet) {
    ClauseSet clauses = {
        {1, 2, 3},
        {-1, 2},
        {-2, 3},
        {1, -3},
        {-1, -2, 3}
    };

    EXPECT_EQ(dpll_rand(clauses), SatState::SAT);
}

TEST(DPLL_RANDTest, UNSATSmallSet) {
    ClauseSet clauses = {
        {1, 2},
        {-1, 2},
        {1, -2, 3},
        {-3},
        {-1}
    };
    EXPECT_EQ(dpll_rand(clauses), SatState::UNSAT);
}

